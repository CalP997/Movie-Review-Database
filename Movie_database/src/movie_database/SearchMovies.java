     /**
     * Find a movie with a given ID
     * @return ResultSet containing movie with the specified ID
     */
     public String[] getMovieDataGivenID(final String movieID) {
        String[] data = new String[3];
        ResultSet movieRS = null;
        MovieDBConnect mc = new MovieDBConnect("movie_db");
        final String getQuery = "SELECT * FROM movie WHERE id = ?";
        //clear txtbox
        txtOutput.setText("");
        try  {
            PreparedStatement pstmt = mc.getConnection().prepareStatement(getQuery);
            pstmt.setString(1, movieID);
            movieRS = pstmt.executeQuery();
        }  catch (SQLException sqle) {
            System.out.println("Exception when  getting movie data: " + sqle.toString());
        }

        try {
            while ((null != movieRS) && (movieRS.next())) {
                String id = movieRS.getString(1);
                int budget = movieRS.getInt(2);
                String homepage = movieRS.getString(3);
                String original_language = movieRS.getString(4);
                String original_title = movieRS.getString(5);
                String overview = movieRS.getString(6);
                float popularity = movieRS.getFloat(7);
                Date release_date = movieRS.getDate(8);
                int revenue = movieRS.getInt(9);
                int runtime = movieRS.getInt(10);
                String release_status = movieRS.getString(11);
                String tagline = movieRS.getString(12);
                String title = movieRS.getString(13);
                float vote_average = movieRS.getFloat(14);
                int vote_count = movieRS.getInt(15);
                txtOutput.append("\n" + movieRS.getInt("id"));
                txtOutput.append("\n" + movieRS.getInt("budget"));
                txtOutput.append("\n" + movieRS.getString("homepage"));
                txtOutput.append("\n" + movieRS.getString("original_language"));
                txtOutput.append("\n" + movieRS.getString("original_title"));
                txtOutput.append("\n" + movieRS.getString("overview"));
                txtOutput.append("\n" + movieRS.getFloat("popularity"));
                txtOutput.append("\n" + movieRS.getDate("release_date"));
                txtOutput.append("\n" + movieRS.getInt("revenue"));
                txtOutput.append("\n" + movieRS.getInt("runtime"));
                txtOutput.append("\n" + movieRS.getString("release_status"));
                txtOutput.append("\n" + movieRS.getString("tagline"));
                txtOutput.append("\n" + movieRS.getString("title"));
                txtOutput.append("\n" + movieRS.getFloat("vote_average"));
                txtOutput.append("\n" + movieRS.getInt("vote_count"));
            }
        } catch (SQLException sqle) {
            System.out.println("Error finding movie: " + sqle.toString());
        }
        return data;
    }
     
