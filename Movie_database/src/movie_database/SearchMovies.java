    private void BtnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnShowActionPerformed
        // TODO add your handling code here:
        MovieDBConnect mc = new MovieDBConnect("movie_db");
        //clear txtbox
        txtOutput.setText("");
        try
        {
            final String retrieveQuery = "SELECT * FROM movie";
            mc.setQuery(retrieveQuery);
            mc.runQuery();
            ResultSet output = mc.getResultSet();
            //Read and display movie info
            if (null != output)
            {
                while(output.next())
                {
                    txtOutput.append("\n" + output.getInt("id"));
                    txtOutput.append(", " + output.getString("title"));
                    txtOutput.append(", " + output.getDate("release_date"));
                }
            }
        }
        catch(SQLException error)
        {
            System.err.println("Error reading records");
        }
    }//GEN-LAST:event_BtnShowActionPerformed

